        <div class="l-inner">
          <h2 class="sub-heading">How to Stay Safe Online?</h2>
          <p>In today’s digital world, staying safe online is more important than ever. Here are some essential tips to help you navigate social media and the internet securely:</p>

          <div class="accordion">
            <div class="accordion-item">
              <h2 class="accordion-header">1. Protect Your Personal Information</h2>
              <div class="accordion-content">
                <p><strong>Think Before You Share:</strong> Be mindful of the information you post online. Avoid sharing personal details such as your full name, address, phone number, school, or any other identifying information.</p>
                <p><strong>Adjust Privacy Settings:</strong> Take the time to review and adjust the privacy settings on your social media accounts. Limit who can see your posts and personal information to only your friends or trusted contacts.</p>
              </div>
            </div>

            <div class="accordion-item">
              <h2 class="accordion-header">2. Create Strong Passwords</h2>
              <div class="accordion-content">
                <p><strong>Use Unique Passwords:</strong> Create strong, unique passwords for each of your online accounts. A good password should be at least 12 characters long and include a mix of letters, numbers, and symbols.</p>
                <p><strong>Enable Two-Factor Authentication:</strong> Whenever possible, enable two-factor authentication (2FA) for an extra layer of security. This requires a second form of verification, such as a text message or authentication app, in addition to your password.</p>
              </div>
            </div>

            <div class="accordion-item">
              <h2 class="accordion-header">3. Be Aware of Cyberbullying</h2>
              <div class="accordion-content">
                <p><strong>Recognize the Signs:</strong> Understand what cyberbullying is and how it can manifest online. If you or someone you know is being bullied, it’s important to talk to a trusted adult.</p>
                <p><strong>Report Inappropriate Behavior:</strong> Most social media platforms have tools to report bullying or harassment. Don’t hesitate to use them if you encounter harmful behavior.</p>
              </div>
            </div>

            <div class="accordion-item">
              <h2 class="accordion-header">4. Think Critically About Friend Requests</h2>
              <div class="accordion-content">
                <p><strong>Know Who You’re Talking To:</strong> Only accept friend requests from people you know in real life. Be cautious of strangers who reach out to you online, as they may not be who they claim to be.</p>
                <p><strong>Verify Profiles:</strong> If you receive a request from someone you think you know, verify their identity through another means before accepting.</p>
              </div>
            </div>

            <div class="accordion-item">
              <h2 class="accordion-header">5. Stay Informed About Online Scams</h2>
              <div class="accordion-content">
                <p><strong>Recognize Scams:</strong> Be aware of common online scams, such as phishing emails or messages that ask for personal information. If something seems too good to be true, it probably is.</p>
                <p><strong>Don’t Click on Suspicious Links:</strong> Avoid clicking on links from unknown sources, as they may lead to harmful websites or downloads.</p>
              </div>
            </div>

            <div class="accordion-item">
              <h2 class="accordion-header">6. Maintain a Positive Digital Footprint</h2>
              <div class="accordion-content">
                <p><strong>Think Before You Post:</strong> Remember that what you post online can last forever. Consider how your posts may be perceived by others, including future employers or colleges.</p>
                <p><strong>Clean Up Your Online Presence:</strong> Regularly review your social media accounts and remove any content that doesn’t reflect who you are today.</p>
              </div>
            </div>

            <div class="accordion-item">
              <h2 class="accordion-header">7. Talk to Someone You Trust</h2>
              <div class="accordion-content">
                <p><strong>Open Communication:</strong> If you ever feel uncomfortable or unsafe online, talk to a trusted adult, friend, or counselor. They can provide support and guidance.</p>
              </div>
            </div>
          </div>
